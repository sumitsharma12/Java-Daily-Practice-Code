package com.Manoj_Sir.Studentlist;

public class Student {
    String name;
    int age;
    int marks;
    int rollnumber;

    public Student(String name, int age, int marks, int rollnumber) {
        this.name = name;
        this.age = age;
        this.marks = marks;
        this.rollnumber = rollnumber;
    }
}
